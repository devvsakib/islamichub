// Get daily hadith (date-seeded rotation)
export function getDailyHadith(date = new Date()) {
  const seed = date.getFullYear() * 10000 + (date.getMonth() + 1) * 100 + date.getDate();
  const index = seed % HADITHS.length;
  return HADITHS[index];
}

// Get hadiths by book
export function getHadithsByBook(book) {
  return HADITHS.filter(h => h.book === book);
}
